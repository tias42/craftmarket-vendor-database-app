    /**
     * Back up database to a binary object file. Asks for confirmation from the user.
     * @param evt ActionEvent, passed by default but not used
     */
    private void btnBackUpDataActionPerformed(java.awt.event.ActionEvent evt)//GEN-FIRST:event_btnBackUpDataActionPerformed
    {//GEN-HEADEREND:event_btnBackUpDataActionPerformed
        int choice = JOptionPane.showConfirmDialog(this, 
                                            "Back up the database?", 
                                            "Confirm", 
                                            JOptionPane.YES_NO_OPTION);
        if (choice == 0)
        {    
            if (DatabaseBackup.writeToFile())
                lblFeedback.setText("Database backup successful");
            else
                lblFeedback.setText("Error backing up database");
        }
    }//GEN-LAST:event_btnBackUpDataActionPerformed
