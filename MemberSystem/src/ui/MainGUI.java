    /**
     * Restore database data from binary file backup. Asks for confirmation from
     * the user.
     * @param evt ActionEvent, passed by default but not used
     */
    private void btnRestoreActionPerformed(java.awt.event.ActionEvent evt)//GEN-FIRST:event_btnRestoreActionPerformed
    {//GEN-HEADEREND:event_btnRestoreActionPerformed
        int choice = JOptionPane.showConfirmDialog(this, 
                                            "Restore the database from the backup?", 
                                            "Confirm", 
                                            JOptionPane.YES_NO_OPTION);
        if (choice == 0)
        {   
            if (DatabaseBackup.readFromFile())
                lblFeedback.setText("Database restored!");
            else
                lblFeedback.setText("Error restoring database");
        }
    }//GEN-LAST:event_btnRestoreActionPerformed
    /**
     * Opens the help document pdf in a separate application
     * @param evt ActionEvent, passed by default but not used
     */
    private void btnHelpActionPerformed(java.awt.event.ActionEvent evt)//GEN-FIRST:event_btnHelpActionPerformed
    {//GEN-HEADEREND:event_btnHelpActionPerformed
        try
        {
            File file = new File(HELPDOC);
            Desktop.getDesktop().open(file);
        }
        catch (IOException ioEx)
        {
            lblFeedback.setText("Error reading Help document");
            System.err.println("Error opening file: " + HELPDOC);
        }
    }//GEN-LAST:event_btnHelpActionPerformed
    /**
     * Set all buttons to disabled except Help
     * For use when the database doesn't load
     */
    private void disableButtons()
    {
        btnAddVendor.setEnabled(false);
        btnViewVendors.setEnabled(false);
        btnBackUpData.setEnabled(false);
        btnRestore.setEnabled(false);
        btnHelp.setEnabled(true);
    }
