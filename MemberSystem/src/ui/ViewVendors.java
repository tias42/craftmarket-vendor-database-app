    /**
     * Get the currently selected vendor information and open a new Update Vendor
     * window displaying its data. Hides this window.
     * @param evt 
     */
    private void btnDetailsActionPerformed(java.awt.event.ActionEvent evt)//GEN-FIRST:event_btnDetailsActionPerformed
    {//GEN-HEADEREND:event_btnDetailsActionPerformed
        if (selectedRow == -1) //check if any vendor has been selected
        {
            lblFeedback.setText("No record selected");
        }
        else
        {
            //Get the current table model (all vendors or search results)
            VendorTableModel currentTable = (VendorTableModel) tblVendors.getModel();
            //get the vendor for the current row and send it to UpdateVendor window
            Vendor v = currentTable.getRow(selectedRow);
            new UpdateVendor(this, v, currentTable);
            this.setVisible(false);
        }
    }//GEN-LAST:event_btnDetailsActionPerformed
    /**
     * Delete the selected vendor.
     * Fetch the vendor corresponding to the selected row and then display a 
     * confirm dialog. If the user confirms, delete the vendor from the database.
     * @param evt 
     */
    private void btnDeleteActionPerformed(java.awt.event.ActionEvent evt)//GEN-FIRST:event_btnDeleteActionPerformed
    {//GEN-HEADEREND:event_btnDeleteActionPerformed
        int vendorID = vendorTableModel.getRow(selectedRow).getId(); //get id of currently selected vendor record
        //confirm with user
        int choice = JOptionPane.showConfirmDialog(this, 
                "Do you really want to delete the vendor with ID #" + vendorID + "?", 
                "Confirm", 
                JOptionPane.YES_NO_OPTION);
        if (choice == 0) //yes option
        {
            //try to delete the vendor
            if (DatabaseMethods.deleteVendor(vendorID))
            {
                lblFeedback.setText("Vendor deleted");
                refresh();
            }
            else //deleting failed
            {
                lblFeedback.setText("Error deleting vendor");
            }
        }
    }//GEN-LAST:event_btnDeleteActionPerformed
    /**
     * Search the vendor data for a matching record based on the search option.
     * Fetch the search option from the search option radio button and set the
     * vendor table view to the search results table model.
     * @param evt 
     */
    private void btnSearchActionPerformed(java.awt.event.ActionEvent evt)//GEN-FIRST:event_btnSearchActionPerformed
    {//GEN-HEADEREND:event_btnSearchActionPerformed
        searchTable.searchVendors(txtSearch.getText(), searchOption);
        tblVendors.setModel(searchTable);
        searchTable.fireTableDataChanged();
        selectedRow = -1;
    }//GEN-LAST:event_btnSearchActionPerformed
