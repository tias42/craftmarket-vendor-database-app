    /**
     * Refresh the table view by re-fetching the database data for both the 
     * all vendors table and search results table
     */
    public void refresh()
    {
        //refresh search table
        this.searchTable.refreshData();
        //refresh vendor table
        this.vendorTableModel.fetchData();
        this.vendorTableModel.fireTableDataChanged();
    }
