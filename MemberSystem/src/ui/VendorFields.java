    /**
     * Find the section in sectionList that matches sectionID and set it as
     * drpSelection's selected item
     * @param sectionID 
     */
    public void setSelectedSection(int sectionID)
    {
        Section s = null;
        for (int i=0; i<sectionList.length; i++)
        {
            if (sectionList[i].getId() == sectionID)
                s = sectionList[i];
        }
        drpSection.setSelectedItem(s); 
    }
