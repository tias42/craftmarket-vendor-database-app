    /**
     * Validate all inputs for the form fields and set feedback text if input
     * is invalid
     * @return true if all text field inputs are valid
     */
    public boolean validateTextInputs()
    {
        boolean valid = true;
        //if validation fails, show feedback text and validation will fail
        if (!Validation.validName(txtFirstName.getText()))
        {
            valid = false;
            lblFirstNameFeedback.setText("First name must be between 1-25 characters");
        }
        else //validation successful, clear feedback text
        {
            lblFirstNameFeedback.setText("");
        }
        if (!Validation.validName(txtLastName.getText()))
        {
            valid = false;
            lblLastNameFeedback.setText("Last name must be between 1-25 characters");
        }
        else
        {
            lblLastNameFeedback.setText("");
        }
        if (!Validation.validShopName(txtShopName.getText()))
        {
            valid = false;
            lblShopNameFeedback.setText("Shop name must be between 1-50 characters");
        }
        else
        {
            lblShopNameFeedback.setText("");
        }
        if (!Validation.validMobileNumber(txtMobile.getText()))
        {
            valid=false;
            lblMobileFeedback.setText("Mobile number must be 10 digits");
        }
        else
        {
            lblMobileFeedback.setText("");
        }
        if (!Validation.validEmailAddress(txtEmail.getText()))
        {
            valid = false;
            lblEmailFeedback.setText("Email address must be in the format: __@__.__");
        }
        else
        {
            lblEmailFeedback.setText("");
        }
        if (radPermanent.isSelected()) // valid fields for permanent members
        {
            if (!Validation.validDate(txtDateJoined.getText()))
            {
                System.out.println(txtDateJoined.getText());
                valid = false;
                lblDateJoinedFeedback.setText("Date must be in the format: yyyy-mm-dd");
            }
            else
            {
                lblDateJoinedFeedback.setText("");
            }
        }
        else //validate fields for day pass members
        {
            if (!Validation.validDate(txtPassDate.getText()))
            {
                valid = false;
                lblPassDateFeedback.setText("Date must be in the format: yyyy-mm-dd");
            }
            else
            {
                lblPassDateFeedback.setText("");
            }
        }    
        return valid;
    }
    /**
     * Fetch the list of sections from the database and use it to populate the 
     * section dropdown control
     * The Section class toString method returns only the section name for this
     * purpose, because the DefaultComboBoxModel uses it
     */
    private void populateSectionDropDown()
    {
        //fetch section data
        ArrayList<Section> sections = DatabaseMethods.getSectionList();
        //create an array from the arraylist of sections
        sectionList = new Section[sections.size()]; 
        for (int i=0; i<sections.size(); i++)
        {
            sectionList[i] = sections.get(i);
        }
        //initialise a combobox model with the section array and assign it
        //to the section combobox
        //each selection item is an object, with the displayed label using the
        //object's toString method
        DefaultComboBoxModel comboModel = new DefaultComboBoxModel(sectionList);
        drpSection.setModel(comboModel);
    }
